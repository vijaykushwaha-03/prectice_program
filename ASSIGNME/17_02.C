#include<stdio.h>
#include<conio.h>

void main()
{
	int n,a[20],i,st,ed;

	clrscr();

	printf("\n\n\t Enter size of array =>");
	scanf("%d",&n);

	for(i=0; i<n; i++)
	{
		printf("\n\n\t Enter number %d =>",i+1);
		scanf("%d",&a[i]);
	}

	for(i=0; i<n; i++)
	{
		printf("\n\t a[%d] is => %d",i+1,a[i]);
	}

	printf("\n\n\t Enter starting point =>");
	scanf("%d",&st);

	printf("\n\n\t Enter ending point =>");
	scanf("%d",&ed);

	for(i=st; i<=ed; i++)
	{
		printf("\n\t a[%d] = %d",i+1,a[i]);
	}
	getch();
}